package br.com.pl.controller;

public class Restricoes {
	private Double valorXRestricao;
	private String operadorRestricao;
	private Double valorYRestricao;
	private String operadorEquacaoRestricao;
	private String resultadoRestricao;
	
	public Double getValorXRestricao() {
		return valorXRestricao;
	}
	public void setValorXRestricao(Double valorXRestricao) {
		this.valorXRestricao = valorXRestricao;
	}
	public String getOperadorRestricao() {
		return operadorRestricao;
	}
	public void setOperadorRestricao(String operadorRestricao) {
		this.operadorRestricao = operadorRestricao;
	}
	public Double getValorYRestricao() {
		return valorYRestricao;
	}
	public void setValorYRestricao(Double valorYRestricao) {
		this.valorYRestricao = valorYRestricao;
	}
	public String getOperadorEquacaoRestricao() {
		return operadorEquacaoRestricao;
	}
	public void setOperadorEquacaoRestricao(String operadorEquacaoRestricao) {
		this.operadorEquacaoRestricao = operadorEquacaoRestricao;
	}
	public String getResultadoRestricao() {
		return resultadoRestricao;
	}
	public void setResultadoRestricao(String resultadoRestricao) {
		this.resultadoRestricao = resultadoRestricao;
	}
	
	
}
